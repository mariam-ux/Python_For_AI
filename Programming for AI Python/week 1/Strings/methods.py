#len length of a string
s1 = '  python'
nbOfChars = len(s1)
print(nbOfChars)

#uper and lowe changes for uper case and lower case
s2 = s1.upper()
s3 = s2.lower()
print(s2)
print(s3)

#strip removes the spaces at the beginning and the end
s4 = s1.strip()
print(s4)

#we can use more than one method at a time
s6 = s1.strip().upper()



